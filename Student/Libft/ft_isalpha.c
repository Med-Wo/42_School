#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char	w;

	w = c;
	if ((w >= 'a' && w <= 'z') ||
		(w >= 'A' && w <= 'Z'))
		return (1);
	else
		return (0);
}
