#include "../../../includes/ft_printf.h"

BOOL	ft_strcmp_c(char *p_src, char p_target)
{
	int	i;

	i = 0;
	if (p_src == NULL || p_target == '\0')
		return (0);
	while (p_src[i] != '\0')
	{
		if (p_src[i] == p_target)
			return (true);
		i++;
	}
	return (false);
}
